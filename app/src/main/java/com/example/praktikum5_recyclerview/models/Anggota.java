package com.example.praktikum5_recyclerview.models;

public class Anggota {
    private String nama, nim;

    public Anggota(String nama, String nim) {
        this.nama = nama;
        this.nim = nim;
    }

    public String getNama() {
        return nama;
    }

    public String getNim() {
        return nim;
    }
}
